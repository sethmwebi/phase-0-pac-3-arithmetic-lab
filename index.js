const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

const increment = (a) => {
  return ++a;
};

const decrement = (a) => {
  return --a;
};

const makeInt = (a) => {
  return parseInt(a, 10);
};

const preserveDecimal = (a) => {
  return parseFloat(a);
};
